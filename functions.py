def multiply():
    return 2 * 3

def cube():
    return 2 ** 3

def square_area(side_length=4):
    area = side_length * 2
    return area

def rectangle_perimeter(width, height):
    perimeter = width * 2 + height * 2
    return perimeter

def maximum(num1,num2):
    if num1 > num2:
        return num1
    else:
        return num2

